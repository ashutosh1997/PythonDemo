# we have a list named days in which seven days in form of strings are stored
days = ['monday', 'tuesday', 'wednesday', 'thursday', 'friday', 'saturday', 'sunday']
print(days)

for i in days:
    print(i)
    print(len(i))

print(i)
print(len(i))

print("\n")

for i in days[3:]:
    print(i)

print("\n")

for i in days[2:5]:
    print(i)

print("\n")

for i in days[:4]:
    print(i)

